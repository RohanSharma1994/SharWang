package aiproj.sharwang;

/**
 *  Vector:
 *    Define a Vector type to store coordinates.
 *
 *  Authors:
 *      Rohan Sharma (rsharma1)
 *      Shiyi Wang (sharwang)
 */

public class Vector {
	public int Row;
	public int Col;

    /**
     * Initialize a Vector
     *
     * @param row
     * @param col
     */
	public Vector(int row, int col) {
		Row = row;
		Col = col;
	}
}